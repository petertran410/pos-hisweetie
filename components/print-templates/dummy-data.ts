/**
 * Thay token trong content bằng dummy data (replicate logic backend replaceVariables).
 */
export function replaceTokensWithDummy(
  content: string,
  templateFor: string,
  itemVariableKeys: Set<string>
): string {
  const data = DUMMY_DATA[templateFor] || {};
  const items = (data.items as any[]) || [];
  let result = content;

  // Replace item rows
  if (itemVariableKeys.size > 0 && items.length > 0) {
    const trRegex = /<tr[^>]*>[\s\S]*?<\/tr>/gi;
    const rows = result.match(trRegex) || [];

    for (const row of rows) {
      const hasItemVar = Array.from(itemVariableKeys).some((k) =>
        row.includes(`{${k}}`)
      );
      if (!hasItemVar) continue;

      const clonedRows = items
        .map((item) => {
          let itemRow = row;
          for (const key of itemVariableKeys) {
            const value = item[key] ?? "";
            itemRow = itemRow.replace(
              new RegExp(`{${key}}`, "g"),
              String(value)
            );
          }
          return itemRow;
        })
        .join("");

      result = result.replace(row, clonedRows);
    }
  }

  // Replace scalar tokens
  for (const [key, value] of Object.entries(data)) {
    if (key !== "items" && !Array.isArray(value)) {
      const regex = new RegExp(`{${key}}`, "g");
      result = result.replace(regex, value?.toString() || "");
    }
  }

  return result;
}
